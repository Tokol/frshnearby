CatalogTranslation _name(String locale, String value) =>
    CatalogTranslation(locale: locale, displayName: value);

CatalogCategory _category(
  String key,
  String english,
  String finnish,
  String swedish,
) => CatalogCategory(
  id: 'category-$key',
  canonicalKey: key,
  translations: {
    'en': _name('en', english),
    'fi': _name('fi', finnish),
    'sv': _name('sv', swedish),
  },
  synonyms: [english, finnish, swedish],
  isActive: true,
);

CatalogProduct _product(
  String key,
  String category,
  String english,
  String finnish,
  String swedish, [
  List<String> synonyms = const [],
]) => CatalogProduct(
  id: 'product-$key',
  canonicalKey: key,
  categoryId: 'category-$category',
  translations: {
    'en': _name('en', english),
    'fi': _name('fi', finnish),
    'sv': _name('sv', swedish),
  },
  synonyms: [english, finnish, swedish, ...synonyms],
  isActive: true,
);

ProductVariant _variant(
  String key,
  String product,
  String english,
  String finnish,
  String swedish, [
  List<String> synonyms = const [],
]) => ProductVariant(
  id: 'variant-$key',
  canonicalKey: key,
  productId: 'product-$product',
  translations: {
    'en': _name('en', english),
    'fi': _name('fi', finnish),
    'sv': _name('sv', swedish),
  },
  synonyms: [english, finnish, swedish, ...synonyms],
  isActive: true,
);

